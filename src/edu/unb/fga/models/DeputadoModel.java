package edu.unb.fga.models;

public class DeputadoModel {

    private String pesquisarDeputados;
    private String nome;
    private String estado;
    private String email;
    private String partido;
    private String telefone;
    private String sexo;
    private String nomeParlamentar;
    private String gabinete;
    private String anexo;
    private String linkFoto;
    private String UF;
    private String legislatura;
    private String NomePartido;
    private String IDPartido;

    public String getPesquisarDeputados() {
        return pesquisarDeputados;
    }

    public void setPesquisarDeputados(String pesquisarDeputados) {
        this.pesquisarDeputados = pesquisarDeputados;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPartido() {
        return partido;
    }

    public void setPartido(String partido) {
        this.partido = partido;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public String getNomeParlamentar() {
        return nomeParlamentar;
    }

    public void setNomeParlamentar(String nomeParlamentar) {
        this.nomeParlamentar = nomeParlamentar;
    }

    public String getGabinete() {
        return gabinete;
    }

    public void setGabinete(String gabinete) {
        this.gabinete = gabinete;
    }

    public String getAnexo() {
        return anexo;
    }

    public void setAnexo(String anexo) {
        this.anexo = anexo;
    }

    public String getLinkFoto() {
        return linkFoto;
    }

    public void setLinkFoto(String linkFoto) {
        this.linkFoto = linkFoto;
    }

    public String getUF() {
        return UF;
    }

    public void setUF(String UF) {
        this.UF = UF;
    }

    public String getLegislatura() {
        return legislatura;
    }

    public void setLegislatura(String legislatura) {
        this.legislatura = legislatura;
    }

    public String getNomePartido() {
        return NomePartido;
    }

    public void setNomePartido(String NomePartido) {
        this.NomePartido = NomePartido;
    }

    public String getIDPartido() {
        return IDPartido;
    }

    public void setIDPartido(String IDPartido) {
        this.IDPartido = IDPartido;
    }

}
